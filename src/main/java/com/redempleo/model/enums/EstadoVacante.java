package com.redempleo.model.enums;

/**
 * Enumeration for vacancy states.
 */
public enum EstadoVacante {
    /**
     * Vacancy has been created and is active.
     */
    CREADA,

    /**
     * Vacancy has been canceled by the company.
     */
    CANCELADA,

    /**
     * Vacancy has been assigned to a candidate.
     */
    ASIGNADA
}